%{
Comandos para usar na Command Window:
- "clear" limpa as variáveis
- "clc" limpa a janela de comando
- "edit nomeDoArquivo" abre o arquivo na janela de edição (com ou sem o .m) 
- "doc nomeDaFunção" mostra a documentação da função
- "save" salva as variáveis em um arquivo .mat no caminho atual 
- "load" carrega as variáveis de um arquivo .mat do caminho atual 
%} 